    private void clearData(JTable jtable){
        
        DefaultTableModel model =  (DefaultTableModel) jtable.getModel();
        
        int removeRows = model.getRowCount();
        
        for (int i = removeRows-1;i>=0;i--){
            model.removeRow(i);
        }
    }
    
    private void jButton_deliveredActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deliveredActionPerformed
        
        //Transfer the selected row's data of pending into delivery
        
        UserAndProductDao userss = new UserAndProductDaoImpl();
        UserAndProduct user = new UserAndProduct();
        
        if(tracking_codes.equals("")|| receiver.equals("")
                || sender.equals("")|| send_from.equals("")
                || destination.equals("")|| date.equals("")
                || weight_items.equals("")){
        
            JOptionPane.showMessageDialog(null, "Select a row to swift into delivered.");
    }else{
           try {
               
            int ids = Integer.parseInt(tracking_codes);
            user.setTracking_code(ids);
            user.setReceiver(receiver);
            user.setSender(sender);
            user.setSend_from(send_from);
            user.setDestination(destination);
            user.setDate(date);
            user.setWeight_item(weight_items);
            
            userss.deleteProduct(user);
            userss.addProduct(user, "deliveryInfo.txt");
            
        } catch (IOException ex) {
            Logger.getLogger(Product_Status.class.getName()).log(Level.SEVERE, null, ex);
        } 
        }
       
        clearData(jTable_pendingInformation1);
        clearData(jTable_deliveryInformation1);
        viewDeliveryData();
        viewPendingData();

        
    }//GEN-LAST:event_jButton_deliveredActionPerformed
    
