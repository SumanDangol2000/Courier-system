     private void viewPendingData() {
    	
    ArrayList dataList;
    UserAndProductDao user = new UserAndProductDaoImpl();

    try {
            dataList = user.viewPending();
            DefaultTableModel dtm = (DefaultTableModel) jTable_pendingInformation1.getModel();
            Object[] lines = dataList.toArray();
            for( int i = 0 ; i<lines.length;i++) {
                    String line = lines[i].toString();
                    String [] row = line.split(",");
                    dtm.addRow(row);
        }


        } catch (IOException e) {

                Logger.getLogger(UserDashboard.class.getName()).log(Level.SEVERE, null, e);
        }
	
    }
 
