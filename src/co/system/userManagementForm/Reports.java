    private int number(String fileName){
        //Create a new file and provide path(location) of a file
        String path="src\\File_collection\\";
        String readLines="";
        int totalCounts=0;
        File file = new File(path+fileName);
        try {
            //Since it is in read mode, data is reading from the file 
            BufferedReader br = new BufferedReader(new FileReader(file));
            while((readLines=br.readLine()) != null){
                totalCounts++;
            }
  
        } catch (FileNotFoundException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE,null,ex);
        } catch (IOException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE,null,ex);
        }

        return totalCounts;
    }
    
