        public void setCurrentDate(JLabel jlabel){
        SimpleDateFormat sdfDate = new SimpleDateFormat("E, MMM dd yyyy | hh:mm:ss aa");
        Timer time = new Timer(500, new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e) {
                jlabel.setText(sdfDate.format(new Date(System.currentTimeMillis())));
            }
        });
        time.setRepeats(true);
        time.start(); 
    }   
    
